
% This script evaluates the coherent information for the specific
% permutation-invariant codes reported in Table 4 of arXiv:2508.09978.


evaluate_code('2-Pauli');
evaluate_code('BB84');
evaluate_code('GADC');
evaluate_code('Damping-Dephasing');


evaluate_code('Dephrasure_q0.1');
evaluate_code('Dephrasure_q0.2');
evaluate_code('Dephrasure_q0.3');
evaluate_code('Dephrasure_q0.4');



function evaluate_code(code_id)
    n = 9; d = 2; k = 2;
    is_qutrit_output = false; 


    fprintf('Evaluating code: %s\n', code_id);
    switch code_id
        case '2-Pauli'
            p_channel = 0.2271;
            px = p_channel/2; pz = px; py = 0;
            K0=sqrt(1-px-py-pz)*eye(2); K1=sqrt(px)*[0 1; 1 0]; K2=sqrt(py)*[0 -1i; 1i 0]; K3=sqrt(pz)*[1 0; 0 -1];
            K={K0, K1, K2, K3};
            RR(:,:,1)=[0.179181326231154, -0.029852749784765-0.382339810616458i; -0.029852749784765+0.382339810616458i, 0.820818673768846];
            RR(:,:,2)=[0.820891385571210,  0.031198830973676-0.382170970063314i;  0.031198830973676+0.382170970063314i, 0.179108614428790];
            p_code = [0.5, 0.5];

        case 'BB84'
            p_channel = 0.112105;
            px = p_channel - p_channel^2; pz=px; py=p_channel^2;
            K0=sqrt(1-px-py-pz)*eye(2); K1=sqrt(px)*[0 1; 1 0]; K2=sqrt(py)*[0 -1i; 1i 0]; K3=sqrt(pz)*[1 0; 0 -1];
            K={K0, K1, K2, K3};
            RR(:,:,1) = [0.140216722802060, 0.019157941056939 + 0.346682804221910i; 0.019157941056939 - 0.346682804221910i, 0.859783277197940];
            RR(:,:,2) = [0.859782464734541, -0.019157594611526 + 0.346683666529354i; -0.019157594611526 - 0.346683666529354i, 0.140217535265459];
            p_code = [0.5, 0.5];

        case 'GADC'
            N_param = 0.1; gamma = 0.44035;
            K1=sqrt(1-N_param)*[1,0;0,sqrt(1-gamma)]; K2=sqrt(N_param)*[sqrt(1-gamma),0;0,1]; K3=sqrt(1-N_param)*[0,sqrt(gamma);0,0]; K4=sqrt(N_param)*[0,0;sqrt(gamma),0];
            K = {K1,K2,K3,K4};
            RR(:,:,1) = [0.156238893536864, 0.271844143105631 - 0.240670247648910i; 0.271844143105631 + 0.240670247648910i, 0.843761106463137];
            RR(:,:,2) = [0.157678276658108, -0.272693678151436 + 0.241761827147194i; -0.272693678151436 - 0.241761827147194i, 0.842321723341892];
            p_code = [0.5, 0.5];

        case 'Damping-Dephasing'
            p_ch = 0.16; g = 0.2;
            K1 = sqrt(1-p_ch)*[1,0;0,sqrt(1-g)]; K2 = sqrt(g)*[0,1;0,0]; K3 = sqrt(p_ch)*[1,0;0,-sqrt(1-g)];
            K = {K1,K2,K3};
            RR(:,:,1) = [0.959730188999771, -0.102654517414096 + 0.167619519612403i; -0.102654517414096 - 0.167619519612403i, 0.040269811000229];
            RR(:,:,2) = [0.959117611049799, 0.101223402231332 - 0.170176819693564i;  0.101223402231332 + 0.170176819693564i, 0.040882388950201];
            p_code = [0.5, 0.5];

        case 'Dephrasure_q0.1'
            is_qutrit_output = true;
            p_channel = 0.32; q = 0.1;
            p_code = [0.121510021946101, 0.878489978053899];
            RR(:,:,1) = [0.241874051397906, -0.021061828117332 - 0.427700080633762i; -0.021061828117332 + 0.427700080633762i, 0.758125948602094];
            RR(:,:,2) = [0.000000032826163, 0.000009517539051 - 0.000180441224635i; 0.000009517539051 + 0.000180441224635i, 0.999999967173837];

        case 'Dephrasure_q0.2'
            is_qutrit_output = true;
            p_channel = 0.24; q = 0.2;
            p_code = [0.0317062697114672, 0.968293730288533];
            RR(:,:,1) = [0.387139187661423, -0.003049422703984 - 0.487086348899087i; -0.003049422703984 + 0.487086348899087i, 0.612860812338577];
            RR(:,:,2) = [0.000000000157627, 0.000002426356153 + 0.000002683810315i; 0.000002426356153 - 0.000002683810315i, 0.999999999842373];
            
        case 'Dephrasure_q0.3'
            is_qutrit_output = true;
            p_channel = 0.16; q = 0.3;
            p_code = [0.0206547189925266, 0.979345281007473];
            RR(:,:,1) = [0.860510660595063, -0.345336265365018 - 0.027836062969306i; -0.345336265365018 + 0.027836062969306i, 0.139489339404937];
            RR(:,:,2) = [0.999999974676378, 0.000157731666750 + 0.000018004665506i; 0.000157731666750 - 0.000018004665506i, 0.000000025323622];

        case 'Dephrasure_q0.4'
            is_qutrit_output = true;
            p_channel = 0.08; q = 0.4;
            p_code = [0.946479051063245, 0.053520948936755];
            RR(:,:,1) = [0.000010563867768, 0.003165425138295 - 0.000737454446143i; 0.003165425138295 + 0.000737454446143i, 0.999989436132232];
            RR(:,:,2) = [0.057435121764494, -0.226643706612390 + 0.052620897025158i; -0.226643706612390 - 0.052620897025158i, 0.942564878235506];

        otherwise
            error('Unknown code_id: %s', code_id);
    end


    R_pure = zeros(d,d,k);
    for i = 1:k
        [V,~] = eig(RR(:,:,i));
        psi = V(:,end); 
        R_pure(:,:,i) = psi * psi';
    end

    if is_qutrit_output
        K0 = sqrt((1-p_channel)*(1-q)) * [1,0;0,1;0,0];
        K1 = sqrt(p_channel*(1-q)) * [1,0;0,-1;0,0];
        K2 = sqrt(q) * [0,0;0,0;1,0];
        K3 = sqrt(q) * [0,0;0,0;0,1];
        K = {K0, K1, K2, K3};
        ci_val = compute_ci_purification_qutrit(p_code, RR, K, n);
        fprintf('   n=%d, p=%.2f, q=%.1f, CI = %.14f\n\n', n, p_channel, q, ci_val);
    else
        ci_val = compute_ci_purification(p_code, R_pure, K, n);
        fprintf('   n=%d, CI = %.14f\n\n', n, ci_val);
    end
end
